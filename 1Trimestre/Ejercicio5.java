//Este algoritmo, visualiza por consola todos los números del 1 al 100.
package traduccionejercicios;
public class Ejercicio_5 {

	public static void main(String[] args) {

		System.out.println("A continuación, se van a visualizar los números del 1 al 100");
		
		int numero;
		numero=1;
		System.out.println(numero);
		
		while (numero<100) {
			numero++;
			System.out.println(numero);
		}
	}
}
